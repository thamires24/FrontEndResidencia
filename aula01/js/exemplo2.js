//igualdade Fraca ou Abstrata
//converte o tipo para comparar
console.log(1 == 1); // true
console.log('1' == 1);
console.log(false==0);
console.log(true==1);

// igualdade Estrita ou Rigorosa
//compara o número, não converte um tipo
console.log(1 === 1); // true
console.log('1' === 1);
console.log(false===0);
console.log(true===1);
